    2. 회사는 전항에 따른 권리자의 요청이 없는 경우라도 권리침해가 인정될 만한 사유가 있거나 기타 회사 정책 및 관련법에 위반되는 경우에는 관련법에 따라 해당 게시물에 대해 임시조치 등을 취할 수 있습니다.

    - 20. 데이터 제출
    1. 회원은 회사가 제시하는 절차와 방법에 따라 데이터를 제출해야 합니다.
    2. 회원은 제1항에 따라 제출한 데이터가 검수를 통해 적합한 데이터로 승인된 경우에만 이에 대한 보상을 지급받을 수 있습니다.
    3. 작업을 진행하는 회원들의 검수 과정에서 해당 데이터가 적합하지 않은 것으로 판정된 데이터는 서비스 품질 향상, 회원의 서비스 이용 환경 개선 및 안정적인 운영을 위한 연구 등에 활용될 수 있습니다. 이 경우 활용되지 않은 데이터는 폐기 처리하는 것을 원칙으로 합니다. 다만, 개인정보의 경우 비식별화조치를 통하여 해당 개인의 권리가 침해되지 않는 범위 내에서만 활용합니다.

