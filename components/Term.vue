    2. 제1항에 따른 검수절차는, 회사가 회원 중에 선정한 1인 또는 다수의 회원이 검수자가 되어 진행합니다. 이 때 과반수 이상이 해당 데이터가 적합하다는 의견으로 일치해야만 적합 판정을 받을 수 있습니다. 만약 두 검수자의 의견이 불일치하는 경우 다른 제3의 검수자 의견에 따라 과반수를 만들어 판정합니다. 검수 진행 방식은 회사 정책에 따라 변경될 수 있습니다.
    3. 회사는 작업 또는 프로젝트의 특성에 따라 회원에게 검수결과에 대한 통지가 필요한 경우를 제외하고 원칙적으로 회원에게 검수결과에 대한 통지의무를 부담하지 않습니다.
    4. 제1항의 검수 작업에 대한 보상은 회사가 정한 정책에 따라 해당 검수를 수행한 검수자에게 지급됩니다.

